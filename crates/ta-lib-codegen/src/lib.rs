fn render_abstract_param_tables(index: usize, function: &FunctionDef) -> String {
    let mut output = String::new();
    let inputs = collapse_required_inputs(function);
    output.push_str(&format!("static ABSTRACT_HANDLE_{index}: u32 = {index}u32;\n"));

    for (param_index, input) in inputs.iter().enumerate() {
        output.push_str(&format!(
            "static ABSTRACT_INPUT_NAME_{index}_{param_index}: &[u8] = b\"{}\\0\";\n",
            escape_c_string(&input.name)
        ));
        let (kind, flags) = match input.kind {
            AbstractInputKind::Price { flags } => ("TA_INPUT_PRICE", flags),
            AbstractInputKind::Real => ("TA_INPUT_REAL", 0),
            AbstractInputKind::Integer => ("TA_INPUT_INTEGER", 0),
        };
        output.push_str(&format!(
            "static ABSTRACT_INPUT_INFO_{index}_{param_index}: TA_InputParameterInfo = TA_InputParameterInfo {{ type_: {kind}, paramName: ABSTRACT_INPUT_NAME_{index}_{param_index}.as_ptr().cast(), flags: {flags} }};\n"
        ));
    }

    for (param_index, opt) in function.optional_inputs.iter().enumerate() {
        output.push_str(&format!(
            "static ABSTRACT_OPT_INPUT_NAME_{index}_{param_index}: &[u8] = b\"{}\\0\";\n",
            escape_c_string(&opt.name)
        ));
        let display = opt
            .short_description
            .as_deref()
            .unwrap_or(opt.name.as_str())
            .to_string();
        output.push_str(&format!(
            "static ABSTRACT_OPT_INPUT_DISPLAY_{index}_{param_index}: &[u8] = b\"{}\\0\";\n",
            escape_c_string(&display)
        ));
        output.push_str(&format!(
            "static ABSTRACT_OPT_INPUT_HINT_{index}_{param_index}: &[u8] = b\"{}\\0\";\n",
            escape_c_string(opt.short_description.as_deref().unwrap_or(""))
        ));
        output.push_str(&format!(
            "static ABSTRACT_OPT_INPUT_INFO_{index}_{param_index}: TA_OptInputParameterInfo = TA_OptInputParameterInfo {{ type_: {kind}, paramName: ABSTRACT_OPT_INPUT_NAME_{index}_{param_index}.as_ptr().cast(), flags: 0, displayName: ABSTRACT_OPT_INPUT_DISPLAY_{index}_{param_index}.as_ptr().cast(), dataSet: core::ptr::null(), defaultValue: {default_value}, hint: ABSTRACT_OPT_INPUT_HINT_{index}_{param_index}.as_ptr().cast(), helpFile: core::ptr::null() }};\n",
            kind = abstract_opt_input_kind(opt),
            default_value = opt_input_default_literal(opt)
        ));
    }

    for (param_index, out) in function.outputs.iter().enumerate() {
        output.push_str(&format!(
            "static ABSTRACT_OUTPUT_NAME_{index}_{param_index}: &[u8] = b\"{}\\0\";\n",
            escape_c_string(&out.name)
        ));
        output.push_str(&format!(
            "static ABSTRACT_OUTPUT_INFO_{index}_{param_index}: TA_OutputParameterInfo = TA_OutputParameterInfo {{ type_: {kind}, paramName: ABSTRACT_OUTPUT_NAME_{index}_{param_index}.as_ptr().cast(), flags: {flags} }};\n",
            kind = abstract_output_kind(out),
            flags = abstract_output_flags(out)
        ));
    }
    output.push('\n');
    output
}

fn render_abstract_lookback_arm(index: usize, function: &FunctionDef) -> String {
    let mut setup = String::new();
    let mut args = Vec::new();
    for (param_index, opt) in function.optional_inputs.iter().enumerate() {
        let var_name = format!("opt_{param_index}");
        match opt.kind.as_str() {
            "Integer" | "MA Type" => setup.push_str(&format!(
                "            let {var_name} = match holder.opt_int({param_index}, {}) {{ Ok(value) => value, Err(ret) => return ret as i32 }};\n",
                opt.default_value.as_deref().unwrap_or("0")
            )),
            "Double" => setup.push_str(&format!(
                "            let {var_name} = match holder.opt_real({param_index}, {}) {{ Ok(value) => value, Err(ret) => return ret as i32 }};\n",
                opt.default_value.as_deref().unwrap_or("0.0")
            )),
            other => panic!(
                "unsupported abstract opt input kind {} for {}",
                other, function.abbreviation
            ),
        }
        args.push(var_name);
    }

    let joined = args.join(", ");
    let call = if joined.is_empty() {
        format!("ta_lib::Core::{}_lookback()", function_rust_name(function))
    } else {
        format!(
            "ta_lib::Core::{}_lookback({joined})",
            function_rust_name(function)
        )
    };

    format!(
        "        {index} => {{\n            if lookback.is_null() {{\n                return RetCode::BadParam as i32;\n            }}\n{setup}            let value = {call};\n            *lookback = value;\n            RetCode::Success as i32\n        }}\n",
        index = index,
        setup = setup,
        call = call
    )
}

fn render_abstract_call_arm(index: usize, function: &FunctionDef) -> String {
    let mut setup = String::new();
    let mut params = vec!["start_idx".to_string(), "end_idx".to_string()];
    let has_price_input = function.required_inputs.iter().any(|arg| {
        matches!(
            arg.kind.as_str(),
            "Open" | "High" | "Low" | "Close" | "Volume" | "Open Interest"
        )
    });
    if has_price_input {
        setup.push_str(
            "            let price_0 = match holder.price_input(0) { Ok(value) => value, Err(ret) => return ret as i32 };\n",
        );
    }
    let mut non_price_input_index = usize::from(has_price_input);
    for arg in &function.required_inputs {
        match arg.kind.as_str() {
            "Open" => params.push("price_0.open".to_string()),
            "High" => params.push("price_0.high".to_string()),
            "Low" => params.push("price_0.low".to_string()),
            "Close" => params.push("price_0.close".to_string()),
            "Volume" => params.push("price_0.volume".to_string()),
            "Open Interest" => params.push("price_0.open_interest".to_string()),
            "Double Array" => {
                let var_name = format!("input_real_{non_price_input_index}");
                setup.push_str(&format!(
                    "            let {var_name} = match holder.real_input({non_price_input_index}) {{ Ok(value) => value, Err(ret) => return ret as i32 }};\n"
                ));
                params.push(var_name);
                non_price_input_index += 1;
            }
            "Integer Array" => {
                let var_name = format!("input_integer_{non_price_input_index}");
                setup.push_str(&format!(
                    "            let {var_name} = match holder.integer_input({non_price_input_index}) {{ Ok(value) => value, Err(ret) => return ret as i32 }};\n"
                ));
                params.push(var_name);
                non_price_input_index += 1;
            }
            other => panic!(
                "unsupported abstract required input kind {} for {}",
                other, function.abbreviation
            ),
        }
    }

    for (param_index, opt) in function.optional_inputs.iter().enumerate() {
        match opt.kind.as_str() {
            "Integer" | "MA Type" => {
                let var_name = format!("opt_integer_{param_index}");
                setup.push_str(&format!(
                    "            let {var_name} = match holder.opt_int({param_index}, {}) {{ Ok(value) => value, Err(ret) => return ret as i32 }};\n",
                    opt.default_value.as_deref().unwrap_or("0")
                ));
                params.push(var_name);
            }
            "Double" => {
                let var_name = format!("opt_real_{param_index}");
                setup.push_str(&format!(
                    "            let {var_name} = match holder.opt_real({param_index}, {}) {{ Ok(value) => value, Err(ret) => return ret as i32 }};\n",
                    opt.default_value.as_deref().unwrap_or("0.0")
                ));
                params.push(var_name);
            }
            other => panic!(
                "unsupported abstract opt input kind {} for {}",
                other, function.abbreviation
            ),
        }
    }

    params.push("out_beg_idx".to_string());
    params.push("out_nb_element".to_string());

    for param_index in 0..function.outputs.len() {
        match function.outputs[param_index].kind.as_str() {
            "Double Array" => {
                let var_name = format!("output_real_{param_index}");
                setup.push_str(&format!(
                    "            let {var_name} = match holder.real_output({param_index}) {{ Ok(value) => value, Err(ret) => return ret as i32 }};\n"
                ));
                params.push(var_name);
            }
            "Integer Array" => {
                let var_name = format!("output_integer_{param_index}");
                setup.push_str(&format!(
                    "            let {var_name} = match holder.integer_output({param_index}) {{ Ok(value) => value, Err(ret) => return ret as i32 }};\n"
                ));
                params.push(var_name);
            }
            other => panic!(
                "unsupported abstract output kind {} for {}",
                other, function.abbreviation
            ),
        }
    }

    format!(
        "        {index} => {{\n{setup}            unsafe {{ TA_{abbr}({params}) }}\n        }},\n",
        index = index,
        abbr = function.abbreviation,
        setup = setup,
        params = params.join(", ")
    )
}

fn abstract_function_flags(function: &FunctionDef) -> i32 {
    function
        .flags
        .iter()
        .fold(0, |flags, flag| {
            flags
                | match flag.as_str() {
                    "Overlap" => 0x01000000,
                    "Volume" => 0x04000000,
                    "Unstable Period" => 0x08000000,
                    "Candlestick" => 0x10000000,
                    _ => 0,
                }
        })
}

fn abstract_opt_input_kind(opt: &ArgumentDef) -> &'static str {
    match opt.kind.as_str() {
        "Integer" => "TA_OPTINPUT_INTEGER_RANGE",
        "Double" => "TA_OPTINPUT_REAL_RANGE",
        "MA Type" => "TA_OPTINPUT_INTEGER_LIST",
        other => panic!("unsupported abstract opt input kind {}", other),
    }
}

fn abstract_output_kind(out: &ArgumentDef) -> &'static str {
    match out.kind.as_str() {
        "Double Array" => "TA_OUTPUT_REAL",
        "Integer Array" => "TA_OUTPUT_INTEGER",
        other => panic!("unsupported abstract output kind {}", other),
    }
}

fn abstract_output_flags(out: &ArgumentDef) -> i32 {
    out.flags.iter().fold(0, |flags, flag| {
        flags
            | match flag.as_str() {
                "Line" => 0x00000001,
                "Dotted Line" => 0x00000002,
                "Dashed Line" => 0x00000004,
                "Dot" => 0x00000008,
                "Histogram" => 0x00000010,
                "Pattern Bool" => 0x00000020,
                "Pattern Bull Bear" => 0x00000040,
                "Pattern Strength" => 0x00000080,
                "Positive" => 0x00000100,
                "Negative" => 0x00000200,
                "Zero" => 0x00000400,
                "Upper Limit" => 0x00000800,
                "Lower Limit" => 0x00001000,
                _ => 0,
            }
    })
}

fn escape_c_string(input: &str) -> String {
    input.replace('\\', "\\\\").replace('"', "\\\"")
}

fn escape_c_ident(input: &str) -> String {
    escape_c_string(input)
}

fn opt_input_default_literal(opt: &ArgumentDef) -> String {
    match opt.kind.as_str() {
        "Integer" | "MA Type" => {
            let value = opt.default_value.as_deref().unwrap_or("0");
            if value.contains('.') {
                value.to_string()
            } else {
                format!("{value}.0")
            }
        }
        "Double" => opt.default_value.as_deref().unwrap_or("0.0").to_string(),
        other => panic!("unsupported opt input default literal kind {}", other),
    }
}

