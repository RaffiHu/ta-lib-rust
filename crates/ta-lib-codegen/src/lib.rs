fn render_function_call_with_optional_renderer(
    function: &FunctionDef,
    start_idx: usize,
    end_idx: usize,
    optional_renderer: fn(&ArgumentDef) -> String,
) -> String {
    let rust_name = function_rust_name(function);
    let mut args = vec![start_idx.to_string(), end_idx.to_string()];

    for (index, argument) in function.required_inputs.iter().enumerate() {
        args.push(render_required_input_expr(argument, index));
    }
    for argument in &function.optional_inputs {
        args.push(optional_renderer(argument));
    }
    args.push("&mut out_beg_idx".to_string());
    args.push("&mut out_nb_element".to_string());
    for argument in &function.outputs {
        args.push(format!("&mut {}", to_snake_case(&argument.name)));
    }

    let formatted_args = args
        .into_iter()
        .map(|arg| format!("            {arg}"))
        .collect::<Vec<_>>()
        .join(",\n");

    format!("context.{rust_name}(\n{formatted_args},\n        )")
}

